function mostrarVentanaFlotante() {
    var ventanaFlotante = document.getElementById("ventanaFlotante");
    ventanaFlotante.style.display = "block";
}

function cerrarVentanaFlotante() {
    var ventanaFlotante = document.getElementById("ventanaFlotante");
    ventanaFlotante.style.display = "none";
}